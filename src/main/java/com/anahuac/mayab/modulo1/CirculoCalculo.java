package com.anahuac.mayab.modulo1;

public class CirculoCalculo {

	public static void main(String[] args) {
		
		double radio = 37.85;
		double area = (radio * radio) * 3.1416;
		double diametro = 2 * radio;
		double circunferencia = 3.1416 * diametro;
		
		System.out.println("La superficie es " + area);
		System.out.println("La diametro es " + diametro);
		System.out.println("La circunferencia es " + circunferencia);
		
	
		

	}

}